"""Asynchronous, off-policy RL scaffolding for verifier-as-reward training.

The synchronous GRPO loop in ``tools/run_rlvr.py`` has a hard barrier every
step: the trainer must wait for a *fresh* batch of rollouts generated by the
*current* policy before it can update. On a real cluster that barrier wastes the
generation fleet — actors sit idle during the optimizer step, and the trainer
sits idle during generation. Async RL systems (DeepSeek's async pipeline,
AReaL, OpenRLHF-async, the "Agent RL" loops where a rollout is a whole tool-using
episode) break the barrier: **generation and training run concurrently**, the
trainer consumes slightly *stale* (off-policy) trajectories from a replay buffer,
and a staleness bound + group-relative advantages keep the off-policy drift safe.

This module implements the *systems policy* of that loop, deterministically and
without a GPU:

- ``grpo_advantages`` — the exact GRPO group-relative advantage
  (``(r - mean)/std`` within a prompt's sample group): zero-mean, scale-normalized,
  order-preserving. This is the math the asynchrony has to stay correct under.
- ``ReplayBuffer`` — bounded FIFO of trajectories tagged with the policy version
  that produced them; enforces a **max-staleness** bound (an over-stale
  trajectory is *dropped*, never trained on) and a capacity bound.
- ``RolloutActor`` — turns a pluggable ``generate_fn`` + the repo's real reward
  seam (``provenance_bench.rl_reward`` / ``math_reward`` / ``code_reward``) into
  GRPO trajectory groups at a given policy version.
- ``simulate`` — a deterministic discrete-event simulation of the decoupled loop
  (``mode="async"``) vs. the barrier loop (``mode="sync"``), so the throughput /
  staleness trade-off is *measured*, not asserted.

Honest scope: ``generate_fn`` and the policy "update" are a synthetic
improvement proxy (a scalar skill that biases sampled completions) so the loop's
*scheduling, buffering, and staleness* behaviour can be exercised in CI. It is
**not** an optimizer and trains no weights — the real GPU GRPO step stays in
``tools/run_rlvr.py`` behind the gated live path. What's real here and reusable
on a cluster: the advantage math, the staleness-bounded buffer, and the
async-vs-sync scheduling model.
"""

from __future__ import annotations

import math
import random
from collections import deque
from dataclasses import dataclass, field
from typing import Any, Callable, Optional


# ---------------------------------------------------------------------------
# GRPO advantages
# ---------------------------------------------------------------------------

def grpo_advantages(rewards: list[float], *, eps: float = 1e-6) -> list[float]:
    """Group-relative advantages: ``A_i = (r_i - mean) / (std + eps)``.

    This is GRPO's critic-free advantage — the baseline is the *group* mean, so
    a sample is "good" only relative to its siblings on the same prompt. Result
    is (near) zero-mean and scale-normalized, which is exactly what keeps the
    gradient stable when the rewards themselves arrive slightly off-policy.
    A degenerate group (all rewards equal) yields all-zero advantages (no signal,
    no blow-up), not a divide-by-zero.
    """
    n = len(rewards)
    if n == 0:
        return []
    mean = sum(rewards) / n
    var = sum((r - mean) ** 2 for r in rewards) / n
    std = math.sqrt(var)
    return [(r - mean) / (std + eps) for r in rewards]


# ---------------------------------------------------------------------------
# Trajectories + replay buffer
# ---------------------------------------------------------------------------

@dataclass
class Trajectory:
    group_id: int
    prompt: str
    completion: str
    reward: float
    advantage: float
    policy_version: int          # version of the policy that GENERATED this


@dataclass
class BufferStats:
    pushed: int = 0
    sampled: int = 0
    dropped_stale: int = 0       # exceeded max_staleness at sample time
    dropped_overflow: int = 0    # evicted by capacity bound before training
    max_staleness_trained: int = 0


class ReplayBuffer:
    """Bounded FIFO of off-policy trajectories with a staleness bound.

    ``capacity`` caps memory (oldest evicted first — those are also the most
    stale). ``max_staleness`` is the largest ``current_version - policy_version``
    gap a trajectory may have *at training time*; older ones are discarded so the
    trainer never updates on data that is too far off-policy.
    """

    def __init__(self, capacity: int, max_staleness: int) -> None:
        if capacity <= 0:
            raise ValueError("capacity must be positive")
        if max_staleness < 0:
            raise ValueError("max_staleness must be >= 0")
        self.capacity = capacity
        self.max_staleness = max_staleness
        self._q: "deque[Trajectory]" = deque()
        self.stats = BufferStats()

    def __len__(self) -> int:
        return len(self._q)

    def push(self, traj: Trajectory) -> None:
        self._q.append(traj)
        self.stats.pushed += 1
        while len(self._q) > self.capacity:
            self._q.popleft()
            self.stats.dropped_overflow += 1

    def push_group(self, trajs: list[Trajectory]) -> None:
        for t in trajs:
            self.push(t)

    def sample(self, batch_size: int, current_version: int) -> list[Trajectory]:
        """Pop up to ``batch_size`` fresh-enough trajectories (FIFO order).

        Trajectories whose staleness exceeds ``max_staleness`` are dropped as
        they're encountered (counted in ``dropped_stale``) and never returned.
        """
        out: list[Trajectory] = []
        while self._q and len(out) < batch_size:
            t = self._q.popleft()
            staleness = current_version - t.policy_version
            if staleness > self.max_staleness:
                self.stats.dropped_stale += 1
                continue
            self.stats.max_staleness_trained = max(
                self.stats.max_staleness_trained, max(0, staleness)
            )
            out.append(t)
        self.stats.sampled += len(out)
        return out


# ---------------------------------------------------------------------------
# Rollout actor (wraps the real reward seam)
# ---------------------------------------------------------------------------

# generate_fn(prompt, policy_version, rng) -> completion text
GenerateFn = Callable[[str, int, random.Random], str]
# reward_fn(completion, case) -> float in [-1, 1]
RewardFn = Callable[[str, dict], float]


class RolloutActor:
    """Produces GRPO trajectory groups: G completions per prompt, scored + advantaged."""

    def __init__(
        self,
        generate_fn: GenerateFn,
        reward_fn: RewardFn,
        *,
        group_size: int = 8,
    ) -> None:
        if group_size < 2:
            raise ValueError("GRPO needs group_size >= 2 for a relative baseline")
        self.generate_fn = generate_fn
        self.reward_fn = reward_fn
        self.group_size = group_size

    def rollout(
        self,
        group_id: int,
        prompt: str,
        case: dict,
        policy_version: int,
        rng: random.Random,
        *,
        spy: Optional[dict] = None,
    ) -> list[Trajectory]:
        completions = [
            self.generate_fn(prompt, policy_version, rng) for _ in range(self.group_size)
        ]
        rewards = []
        for c in completions:
            rewards.append(self.reward_fn(c, case))
            if spy is not None:
                spy["reward_calls"] = spy.get("reward_calls", 0) + 1
        advs = grpo_advantages(rewards)
        return [
            Trajectory(group_id, prompt, c, r, a, policy_version)
            for c, r, a in zip(completions, rewards, advs)
        ]


# ---------------------------------------------------------------------------
# Synthetic policy + default generate/reward (improvement proxy for the sim)
# ---------------------------------------------------------------------------

@dataclass
class PolicyState:
    """A scalar 'skill' in [0,1]. Higher skill -> more high-reward completions.

    A stand-in for model weights so the *loop* can be measured. The trainer
    nudges skill toward the fraction of positive-advantage samples in a batch —
    a coarse proxy for "the gradient moves probability mass to better completions".
    """

    version: int = 0
    skill: float = 0.1

    def update(self, batch: list[Trajectory], lr: float) -> None:
        if not batch:
            self.version += 1
            return
        good = sum(1 for t in batch if t.advantage > 0) / len(batch)
        self.skill = min(1.0, self.skill + lr * good)
        self.version += 1


def scripted_generate(prompt: str, policy_version: int, rng: random.Random) -> str:
    """Emit 'good' or 'bad' with probability rising in the encoded skill.

    The skill is smuggled via the prompt suffix ``|skill=<f>`` so ``generate_fn``
    stays the pure ``(prompt, version, rng)`` shape an actor expects.
    """
    skill = 0.1
    if "|skill=" in prompt:
        try:
            skill = float(prompt.rsplit("|skill=", 1)[1])
        except ValueError:
            skill = 0.1
    return "good" if rng.random() < skill else "bad"


def scripted_reward(completion: str, case: dict) -> float:
    return 1.0 if completion == "good" else -1.0


# ---------------------------------------------------------------------------
# Discrete-event simulation: async (decoupled) vs sync (barrier)
# ---------------------------------------------------------------------------

@dataclass
class SimReport:
    mode: str
    ticks: int
    trained_trajectories: int
    train_steps: int
    trainer_idle_ticks: int
    final_version: int
    final_skill: float
    initial_skill: float
    max_staleness_trained: int
    dropped_stale: int
    dropped_overflow: int

    def as_dict(self) -> dict:
        return {k: getattr(self, k) for k in self.__dataclass_fields__}


def simulate(
    *,
    mode: str = "async",
    ticks: int = 200,
    n_actors: int = 4,
    group_size: int = 8,
    batch_size: int = 16,
    gen_latency: int = 3,
    capacity: int = 256,
    max_staleness: int = 2,
    lr: float = 0.05,
    seed: int = 0,
) -> SimReport:
    """Deterministically simulate the decoupled RL loop.

    ``mode="async"``: actors generate continuously (pipelined over ``gen_latency``
    ticks); the trainer consumes a batch every tick the buffer can fill one,
    tolerating staleness up to ``max_staleness``. ``mode="sync"``: a barrier loop —
    the trainer launches a wave, idles ``gen_latency`` ticks while it generates at
    the current version (zero staleness), then trains once and bumps the version.

    The same seed reproduces the run exactly (CI-stable).
    """
    if mode not in ("async", "sync"):
        raise ValueError("mode must be 'async' or 'sync'")
    rng = random.Random(seed)
    policy = PolicyState()
    initial_skill = policy.skill
    actor = RolloutActor(scripted_generate, scripted_reward, group_size=group_size)
    buf = ReplayBuffer(capacity=capacity, max_staleness=max_staleness)
    trained = 0
    train_steps = 0
    idle = 0
    gid = 0

    def make_group(version: int) -> list[Trajectory]:
        nonlocal gid
        gid += 1
        prompt = f"case-{gid % 7}|skill={policy.skill:.4f}"
        return actor.rollout(gid, prompt, {}, version, rng)

    if mode == "async":
        # Each actor is busy until its finish tick, then pushes a group stamped
        # with the version it STARTED at (that's where the staleness comes from).
        finish = [gen_latency] * n_actors          # all start a job at tick 0
        start_ver = [policy.version] * n_actors
        for tick in range(ticks):
            for a in range(n_actors):
                if finish[a] == tick:
                    buf.push_group(make_group(start_ver[a]))
                    finish[a] = tick + gen_latency  # immediately start the next
                    start_ver[a] = policy.version
            if len(buf) >= batch_size:
                batch = buf.sample(batch_size, policy.version)
                policy.update(batch, lr)
                trained += len(batch)
                train_steps += 1
            else:
                idle += 1
    else:  # sync barrier loop
        tick = 0
        while tick < ticks:
            # Launch a synchronized wave at the current version; trainer idles
            # while the fleet generates.
            idle += gen_latency
            tick += gen_latency
            if tick >= ticks:
                break
            for _ in range(n_actors):
                buf.push_group(make_group(policy.version))  # zero staleness
            batch = buf.sample(batch_size, policy.version)
            policy.update(batch, lr)
            trained += len(batch)
            train_steps += 1
            # On-policy: any rollouts not consumed this wave are discarded rather
            # than carried to a later (now-stale) version. This is the cost the
            # barrier pays — and exactly why async keeps a replay buffer instead.
            buf.stats.dropped_overflow += len(buf)
            buf._q.clear()
            tick += 1

    return SimReport(
        mode=mode,
        ticks=ticks,
        trained_trajectories=trained,
        train_steps=train_steps,
        trainer_idle_ticks=idle,
        final_version=policy.version,
        final_skill=round(policy.skill, 4),
        initial_skill=initial_skill,
        max_staleness_trained=buf.stats.max_staleness_trained,
        dropped_stale=buf.stats.dropped_stale,
        dropped_overflow=buf.stats.dropped_overflow,
    )


# ---------------------------------------------------------------------------
# Offline invariants
# ---------------------------------------------------------------------------

def offline_invariants() -> "tuple[bool, dict]":
    checks: dict[str, bool] = {}

    # 1. GRPO advantages: zero-mean, order-preserving, degenerate-safe.
    advs = grpo_advantages([1.0, -1.0, 1.0, -1.0])
    checks["adv_zero_mean"] = abs(sum(advs)) < 1e-6
    checks["adv_order_preserving"] = advs[0] > advs[1]
    checks["adv_degenerate_safe"] = grpo_advantages([0.5, 0.5, 0.5]) == [0.0, 0.0, 0.0]

    # 2. Replay buffer drops over-stale trajectories (never trains them).
    buf = ReplayBuffer(capacity=100, max_staleness=2)
    for v in range(6):
        buf.push(Trajectory(v, "p", "c", 1.0, 0.0, policy_version=v))
    got = buf.sample(10, current_version=5)          # staleness = 5 - v
    checks["stale_dropped"] = all(5 - t.policy_version <= 2 for t in got)
    checks["stale_counted"] = buf.stats.dropped_stale == 3   # v=0,1,2 too stale

    # 3. Capacity bound holds (oldest overflow evicted).
    cb = ReplayBuffer(capacity=4, max_staleness=99)
    for v in range(10):
        cb.push(Trajectory(v, "p", "c", 1.0, 0.0, policy_version=v))
    checks["capacity_bounded"] = len(cb) == 4 and cb.stats.dropped_overflow == 6

    # 4. Reward seam is actually invoked by the actor.
    spy: dict = {}
    actor = RolloutActor(scripted_generate, scripted_reward, group_size=4)
    grp = actor.rollout(0, "case|skill=0.5", {}, 0, random.Random(0), spy=spy)
    checks["reward_seam_invoked"] = spy.get("reward_calls") == 4
    checks["group_advantages_zero_mean"] = abs(sum(t.advantage for t in grp)) < 1e-6

    # 5. Determinism: same seed → identical report.
    r_a = simulate(mode="async", seed=7)
    r_b = simulate(mode="async", seed=7)
    checks["deterministic"] = r_a.as_dict() == r_b.as_dict()

    # 6. Async vs sync trade-off (the whole point):
    a = simulate(mode="async", ticks=300, seed=1)
    s = simulate(mode="sync", ticks=300, seed=1)
    checks["async_higher_throughput"] = a.trained_trajectories > s.trained_trajectories
    checks["async_less_idle"] = a.trainer_idle_ticks < s.trainer_idle_ticks
    checks["sync_zero_staleness"] = s.max_staleness_trained == 0
    checks["async_bounded_staleness"] = (
        0 < a.max_staleness_trained <= 2
    )
    # 7. The loop still learns under asynchrony (synthetic improvement proxy).
    checks["async_policy_improves"] = a.final_skill > a.initial_skill
    checks["never_trained_beyond_bound"] = a.max_staleness_trained <= 2

    ok = all(checks.values())
    return ok, {
        "checks": checks,
        "async": a.as_dict(),
        "sync": s.as_dict(),
    }


if __name__ == "__main__":
    ok, detail = offline_invariants()
    print("Async-RL offline invariants:", "PASS" if ok else "FAIL")
    for k, v in detail["checks"].items():
        print(f"  [{'ok' if v else 'XX'}] {k}")
    print("  async:", detail["async"])
    print("  sync :", detail["sync"])
    raise SystemExit(0 if ok else 1)
